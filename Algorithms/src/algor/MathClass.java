package algor;

public class MathClass {
	
	public static void main(String[] args) {
		
		System.out.println(Math.E);
		System.out.println(Math.PI);
		System.out.println(Math.abs(5-12)); //returns positive value
		System.out.println(Math.acos(1/2)); //arccos
		System.out.println(Math.decrementExact(-135));
		System.out.println(Math.floor(-125));
		System.out.println(Math.max(125, 126));   //126
		System.out.println(Math.negateExact(125));  //-125
		
	}

}
